#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - subtract two integers
 * @a: first int
 * @b: second int
 * Return: Modula
 */
int mod(int a, int b)
{
        return (a % b);
}
